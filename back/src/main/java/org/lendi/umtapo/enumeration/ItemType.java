package org.lendi.umtapo.enumeration;

/**
 * Item types.
 */
public enum ItemType {
    /**
     * Book item type.
     */
    BOOK,
    /**
     * Audio recording item type.
     */
    AUDIO_RECORDING,
    /**
     * Music sheet item type.
     */
    MUSIC_SHEET,
    /**
     * Video recording item type.
     */
    VIDEO_RECORDING,
    /**
     * Initial item added in db to set internalId.
     */
    INITIAL_ITEM
}
