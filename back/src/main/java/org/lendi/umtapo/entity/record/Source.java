package org.lendi.umtapo.entity.record;

/**
 * Source entity.
 */
public class Source {
    private String library;
    private String url;

    public String getLibrary() {
        return library;
    }

    public void setLibrary(String library) {
        this.library = library;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
